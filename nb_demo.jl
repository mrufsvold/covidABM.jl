# ╔═╡ b0186ac0-7de6-4ed9-bd4c-9913dc9360de
const adata = [:stage]

# ╔═╡ 75498a81-eeb1-4ddd-9126-fa91ccbff96c
begin
	model = make_model(properties)
	agent_df = run!(
        model, 
        person.agent_step!, 
        network.model_step!, 
        100; 
        adata=adata, 
        agents_first=false)
end

